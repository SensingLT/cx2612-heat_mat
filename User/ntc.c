/**
 * @brief 12位ADC转温度，输出×10（如250=25.0℃）
 * @param adc12 原始12位ADC采样值
 * @return int16_t 温度×10，超出范围返回边界值
 */
int16_t NTC_AdcToTemp(uint16_t adc12)
{
    uint16_t adc_compress, left, right, mid;
    uint16_t hit_idx;
    int16_t temp_x10;

    // 1. 原始ADC ÷6 四舍五入：(val + 除数/2) / 除数
    adc_compress = (adc12 + 3) / 6;

    // 边界判断：大于501 → <0℃；小于61 → >100℃
    if (adc_compress > ntc_adc_table[0])
    {
        return 0;   // 低于0℃限幅输出0.0℃
    }
    if (adc_compress < ntc_adc_table[430])
    {
        return 1000;// 高于100℃限幅输出100.0℃
    }

    // 2. 二分查找压缩ADC在平铺数组下标 0~430
    left = 0;
    right = 430;
    while (right - left > 1)
    {
        mid = (left + right) >> 1;
        // 数组降序：adc更大 → 温度更低，向左区间
        if (adc_compress > ntc_adc_table[mid])
        {
            right = mid;
        }
        else
        {
            left = mid;
        }
    }
    hit_idx = left;

    // 3. 二分查找hit_idx所属温度段（替代顺序for循环，速度更快）
    uint16_t T_left = 0, T_right = 100, T = 0;
    while (T_right - T_left > 1)
    {
        uint16_t T_mid = (T_left + T_right) >> 1;
        if (hit_idx >= temp_start_idx[T_mid])
        {
            T_left = T_mid;
        }
        else
        {
            T_right = T_mid;
        }
    }
    T = T_left;

    // 4. 获取当前温度段首尾下标
	uint16_t seg_start = temp_start_idx[T];
	uint16_t seg_end;
	if(T >= 100)
		seg_end = temp_start_idx[T];
	else
		seg_end   = temp_start_idx[T+1] - 1;

    // 单元素段（99/100℃）无插值，直接返回整数温度
    if (seg_start == seg_end)
    {
        return (int16_t)T * 10;
    }

    uint16_t adc_start = ntc_adc_table[seg_start];
    uint16_t adc_end   = ntc_adc_table[seg_end];
    uint16_t delta_adc = adc_start - adc_end;
    uint16_t offset    = adc_start - adc_compress;

    temp_x10 = (int16_t)T * 10;
    // 插值四舍五入
    int16_t interp = (int16_t)((offset * 10 + delta_adc / 2) / delta_adc);
    temp_x10 += interp;

    return temp_x10;
}