int16_t NTC_AdcToTemp(uint16_t adc12)
{
    uint16_t left, right, mid;
    int16_t temp_x10;

    if (adc12 >= ntc_adc_table[0]) {
        return -300;  // ADC 大于等于 -30°C 对应的值 → ≤ -30°C
    }
    if (adc12 <= ntc_adc_table[380]) {
        return 3500;  // ADC 小于等于 350°C 对应的值 → ≥ 350°C
    }


    left = 0;
    right = 380;
    while (right - left > 1) {
        mid = (left + right) >> 1;
        if (adc12 > ntc_adc_table[mid]) {
            right = mid;   // 目标值更大 → 往左（温度更低）
        } else {
            left = mid;    // 目标值更小 → 往右（温度更高）
        }
    }

    /* 线性插值（注意 delta_adc 为负，需取绝对值） */
    uint16_t delta_adc = ntc_adc_table[left] - ntc_adc_table[left + 1];  // 正值
    uint16_t offset = ntc_adc_table[left] - adc12;  // 距离左边界的差值

    temp_x10 = (int16_t)((-30 + (int16_t)left) * 10);
    int16_t interp = (int16_t)((offset * 10 + delta_adc / 2) / delta_adc);
    temp_x10 += interp;

    return temp_x10;
}


